/*
al presionar el botón mostrar 10 repeticiones 
con números ASCENDENTE, desde el 1 al 10.*/
function mostrar()
{
	var i; // Variable de control 

	i=1;

	while (i<11) //Condición
	{ 
		alert(i);
		i=i+1; //Modificación de la variable de control. También se puede poner i++
	}

	alert ("Aca terminó el bucle");
}

